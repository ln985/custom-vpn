    /** 获取所有省名 */
    fun provinceNames(): List<String> = allProvinces.map { it.name }

    /** 获取指定省的所有城市名 */
    fun cityNames(province: String): List<String> =
        allProvinces.find { it.name == province }?.cities?.map { it.name } ?: emptyList()

    /** 获取指定省、市的所有区名 */
    fun districtNames(province: String, city: String): List<District> =
        allProvinces.find { it.name == province }
            ?.cities?.find { it.name == city }
            ?.districts ?: emptyList()
